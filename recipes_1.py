def создать_рецепт():
    рецепты = {}

    while True:
        название_блюда = input("Введите название блюда (или 'выход' для завершения): ")
        if название_блюда == "выход":
            break

        количество_ингредиентов = int(input("Введите количество ингредиентов в блюде: "))

        ингредиенты = []
        for i in range(количество_ингредиентов):
            ингредиент = input("Введите название ингредиента: ")
            количество = input("Введите количество ингредиента: ")
            единица_измерения = input("Введите единицу измерения: ")
            ингредиенты.append(f"{ингредиент} | {количество} | {единица_измерения}")

        рецепты[название_блюда] = ингредиенты

    with open("рецепты.txt", "w") as файл:
        for название_блюда, ингредиенты in рецепты.items():
            файл.write(название_блюда + "\n")
            файл.write(str(len(ингредиенты)) + "\n")
            for ингредиент in ингредиенты:
                файл.write(ингредиент + "\n")
            файл.write("\n")

    print("Рецепты успешно сохранены в файле 'рецепты.txt'!")

создать_рецепт()
